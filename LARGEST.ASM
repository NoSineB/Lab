DATA SEGMENT
	A db 05h,02h,12h,03h
	RES db ?
	
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV CX,04H
		MOV AX,0000H
		MOV BL,00H
		LEA SI,A
		UP:
		MOV AL,A[SI]
		CMP AL,BL
		JL NXT
		MOV BL,AL
		NXT:
		INC SI
		DEC CX
		JNZ UP
		
		MOV RES,BL
		INT 03H
		CODE ENDS 
		END START
